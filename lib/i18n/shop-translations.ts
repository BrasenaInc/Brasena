export type Translations = typeof shopTranslations.en;

function getNested(obj: Record<string, unknown>, path: string): string {
  const parts = path.split(".");
  let current: unknown = obj;
  for (const part of parts) {
    current = (current as Record<string, unknown>)?.[part];
  }
  return typeof current === "string" ? current : path;
}

export function createShopT(lang: ShopLang) {
  const dict = shopTranslations[lang] as Record<string, unknown>;
  return (path: string) => getNested(dict, path);
}

export type ShopT = ReturnType<typeof createShopT>;
